local function createText(text, fontCfg) -- textWidth, chapterSize, contentSize
    local str_size, alignment

    local chapterIdx = string.find(text,"##") -- 判断是否是章节标题
    if chapterIdx then
        local str_content = string.split(text,"##")
        text = str_content[2]
        str_size = fontCfg.chapterSize or chapterFontCfg.fontSize
        alignment = chapterFontCfg.Alignment
    else
        str_size = fontCfg.contentSize or contentFontCfg.fontSize
        alignment = contentFontCfg.Alignment
    end
    local content = cc.Label:createWithTTF(text, commonFontCfg.fontName, str_size)
    content:setWidth(fontCfg.textWidth) -- 通过此方法可以设置最大宽度, 同时其contentSize也为自动适应的大小
    content:setColor(commonFontCfg.color)
    content:setOpacity(fontCfg.opacity or commonFontCfg.opacity)
    content:setAlignment(alignment) -- 对齐方式详见display.lua
    if fontCfg.lineHeight then
        content:setLineHeight(fontCfg.lineHeight)
    end

    local widget = ccui.Widget:create() -- 用widget包裹, 这样才可以加入到listView中
    local size = content:getContentSize()
    widget:setContentSize(size)
    content:addTo(widget)
    content:setPosition(cc.p(size.width * 0.5, size.height * 0.5))
    return widget
end

