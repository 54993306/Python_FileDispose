        content:setColor(commonFontCfg.color)
        content:setOpacity(fontCfg.opacity or commonFontCfg.opacity)
        content:setAnchorPoint(cc.p(1, 0)) -- 右下角
        local bg = ccui.Helper:seekWidgetByName(self.m_pWidget, "Image_bg")
        local bgSize = bg:getContentSize()
        content:addTo(bg)
        content:setPosition(cc.p(bgSize.width - 40, 70))
    end
    return content
end

function Duty:onInit()
    self.baseShowType = UIWndBase.BaseShowType.RTOL
    if not IsPortrait then -- TODO
        self:addWidgetClickFunc(self.m_pWidget, handler(self, self.popSelf))
    end

    local keyback = function(widget, touchType)
        if touchType == ccui.TouchEventType.ended then
            if IsPortrait then -- TODO
                SoundManager.playEffect("btn");
            end
            self.keyBack()
            if self.m_data.closeCallback then
                self.m_data.closeCallback()
            end
        end
    end

    local btn_close = ccui.Helper:seekWidgetByName(self.m_pWidget, "btn_close")
    btn_close:addTouchEventListener(keyback)

    local btn_sure = ccui.Helper:seekWidgetByName(self.m_pWidget, "btn_sure")
    btn_sure:addTouchEventListener(keyback)
    
    if self.m_data.type == Duty.DIALOG_TYPE.AGREEMENT then
        self.m_data.textTab = userContent
    elseif self.m_data.type == Duty.DIALOG_TYPE.SERVER_NOTICE then
        self.m_data = ServerNoticeCfg.getData(self.m_data)
        self:createOfficalName( "—" .. GC_GameName .. "官方", self.m_data.fontCfg)
    else
        self.m_data.textTab = self.m_data.textTab or DutyContent
    end

    local lab_title = ccui.Helper:seekWidgetByName(self.m_pWidget, "lab_title")
    if self.m_data.title then
        lab_title:setString(self.m_data.title)
    end

    local fontCfg = self.m_data.fontCfg or {}
    local list = ccui.Helper:seekWidgetByName(self.m_pWidget, "list")
    fontCfg.textWidth = list:getContentSize().width - 2 * commonFontCfg.widthOff
    for _,text in pairs(self.m_data.textTab) do
        list:pushBackCustomItem( createText( text, fontCfg) )
    end
end

return Duty
