    call = function(str, idx)
        if idx > #argTab then return str end

        if(str==nil or str=="")then return {} end
        local retTable
        if argTab[idx] == '' then
            retTable = {str}
        else
            retTable = Util.split(str, argTab[idx]);
        end        

        for i,v in ipairs(retTable) do
            retTable[i] = call(v, idx + 1)
        end
        return retTable
    end

    return call(strText, 1)
end

