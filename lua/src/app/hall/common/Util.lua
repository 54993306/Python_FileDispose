Util.split=function( str, splitchar )
    local arr = {}
    local head = 1
    local tail = nil
    while true do
        tail, _ = str:find(splitchar,head )
        if tail == nil then
            arr[ 1 + #arr ] = str:sub( head, -1 )
            break
        end
        arr[ 1 + #arr ] = str:sub( head, tail - 1 )
        head = tail + 1
    end
    return arr
end

