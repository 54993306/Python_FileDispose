function _gamePlayMjEffect(sex, card)
    if card > 10 and card < 20 then
        _GameEffectMjWan(sex, card%10);
    elseif card > 20 and card < 30 then
        _GameEffectMjTiao(sex, card%10);
    elseif card > 30 and card < 40 then
        _GameEffectMjTong(sex, card%10);
    elseif card > 40 and card < 50 then
        _GameEffectMjFeng(sex, card%10);
    end
end

